class stock:
    def __init__(self, symbol, companyName, high, low, latestPrice, latestSource, calculationPrice, stockOpen, openTime, close,):
        self.symbol = symbol
        self.companyName = companyName
        self.high = high
        self.low = low
        self.latestPrice = latestPrice
        self.latestSource = latestSource
        self.calculationPrice = calculationPrice
        self.stockOpen = stockOpen
        self.openTime = openTime
        self.close = close
